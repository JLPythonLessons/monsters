class mantois:
    def __init__(self, level=0, name="mantois", hp=16, strength=4, defense=1, accuracy=10, resistance=0, team_skill_strength=0, power=2):
        self.level = level
        self.name = name
        self.power = power
        self.hp = hp + (level * power)
        self.strength = strength + (level * power)
        self.defense = defense + (level * power)
        self.accuracy = accuracy + (level * power)
        self.resistance = resistance + (level * power)
        self.team_skill_strength = team_skill_strength + (level * power)


